async function getOrCreateIngredient(name: string): Promise<string> {
  const normalizedName = name.toLowerCase().trim();
  
  let ingredient = await prisma.ingredient.findUnique({
    where: { name: normalizedName }
  });

  if (!ingredient) {
    ingredient = await prisma.ingredient.create({
      data: { name: normalizedName }
    });
  }

  return ingredient.id;
}

async function getOrCreateTag(name: string): Promise<string> {
  const normalizedName = name.toLowerCase().trim();
  
  let tag = await prisma.tag.findUnique({
    where: { name: normalizedName }
  });

  if (!tag) {
    tag = await prisma.tag.create({
      data: { name: normalizedName }
    });
  }

  return tag.id;
}

async function getOrCreateAllergen(name: string): Promise<string> {
  const normalizedName = name.toLowerCase().trim();
  
  let allergen = await prisma.allergen.findUnique({
    where: { name: normalizedName }
  });

  if (!allergen) {
    allergen = await prisma.allergen.create({
      data: { name: normalizedName }
    });
  }

  return allergen.id;
}

async function addDishes() {
  console.log('  Adding 150 new dishes to the database...\n');

  let added = 0;
  let skipped = 0;
  let errors = 0;

  for (const dishData of newDishes) {
    try {
      // Check if dish already exists
      const existing = await prisma.dish.findFirst({
        where: {
          name: dishData.name,
          slotCategory: dishData.slotCategory
        }
      });

      if (existing) {
        console.log(`    Skipped (exists): ${dishData.name}`);
        skipped++;
        continue;
      }

      // Create the dish
      const dish = await prisma.dish.create({
        data: {
          name: dishData.name,
          slotCategory: dishData.slotCategory,
          kosher: dishData.kosher,
          kosherStyle: dishData.kosherStyle,
          difficulty: dishData.difficulty,
          mainProtein: dishData.mainProtein,
          cuisine: dishData.cuisine,
          prepTimeMinutes: dishData.prepTimeMinutes,
          cookTimeMinutes: dishData.cookTimeMinutes,
          servings: dishData.servings,
          sourceUrl: dishData.sourceUrl,
        }
      });

      // Add ingredients
      for (const ingredientName of dishData.ingredients) {
        const ingredientId = await getOrCreateIngredient(ingredientName);
        await prisma.dishIngredient.create({
          data: {
            dishId: dish.id,
            ingredientId: ingredientId
          }
        });
      }

      // Add tags
      for (const tagName of dishData.tags) {
        const tagId = await getOrCreateTag(tagName);
        await prisma.dishTag.create({
          data: {
            dishId: dish.id,
            tagId: tagId
          }
        });
      }

      // Add allergens
      for (const allergenName of dishData.allergens) {
        const allergenId = await getOrCreateAllergen(allergenName);
        await prisma.dishAllergen.create({
          data: {
            dishId: dish.id,
            allergenId: allergenId
          }
        });
      }

      const kosherStatus = dishData.kosher ? '' : '';
      console.log(`   ${kosherStatus} Added: ${dishData.name} (${dishData.slotCategory})`);
      added++;
    } catch (error) {
      console.error(`   Error adding ${dishData.name}:`, error);
      errors++;
    }
  }

  // Final summary
  const totalDishes = await prisma.dish.count();
  const kosherDishes = await prisma.dish.count({ where: { kosher: true } });
  const nonKosherDishes = totalDishes - kosherDishes;

  console.log('\n' + '='.repeat(60));
  console.log(' Import Summary:');
  console.log(`   Added: ${added}`);
  console.log(`   Skipped: ${skipped}`);
  console.log(`   Errors: ${errors}`);
  console.log('\n Database Summary:');
  console.log(`   Total dishes: ${totalDishes}`);
  console.log(`   Kosher dishes: ${kosherDishes}`);
  console.log(`   Non-kosher dishes: ${nonKosherDishes}`);

  // Category breakdown
  const categories = ['main_chicken', 'main_beef', 'side_veg', 'side_starch', 'soup', 'muffin'];
  console.log('\n Category Breakdown:');
  for (const cat of categories) {
    const count = await prisma.dish.count({ where: { slotCategory: cat } });
    const kosherCount = await prisma.dish.count({ where: { slotCategory: cat, kosher: true } });
    console.log(`   ${cat}: ${count} total (${kosherCount} kosher)`);
  }
}

addDishes()
  .catch((error) => {
    console.error(' Error:', error);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
